package com.example.helper.activity;

import com.example.helper.activity.lifecycle.IActivityLifecycle;

/**
 * 
 * @author ufuchs
 *
 * @param <E>
 */
public class ActivityGuard<E> {

	////////////////////////////////////////////////////////////////////////////
	// fields
	////////////////////////////////////////////////////////////////////////////
	
	private IActivityLifecycle lifeCycle;
	
	private E events;
	
	////////////////////////////////////////////////////////////////////////////
	// life cycle
	////////////////////////////////////////////////////////////////////////////
	
	public ActivityGuard(IActivityLifecycle lifeCycle, E events) {
		this.lifeCycle = lifeCycle;
		this.events = events;
	}
	
	////////////////////////////////////////////////////////////////////////////
	// getters
	////////////////////////////////////////////////////////////////////////////
	
	public IActivityLifecycle getLifeCycle() {
		return lifeCycle;
	}

	public E getEvents() {
		return events;
	}
	
}
